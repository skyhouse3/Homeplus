package com.kh.homeplus.manpower.model.vo;

public class ManpowerExpense {
	private int mExpense16;
	private int mExpense17;
	private int mExpense18;
	private int mExpense19;
	private int mExpense20;
	
	public ManpowerExpense() {}

	public ManpowerExpense(int mExpense16, int mExpense17, int mExpense18, int mExpense19, int mExpense20) {
		super();
		this.mExpense16 = mExpense16;
		this.mExpense17 = mExpense17;
		this.mExpense18 = mExpense18;
		this.mExpense19 = mExpense19;
		this.mExpense20 = mExpense20;
	}

	public int getmExpense16() {
		return mExpense16;
	}

	public void setmExpense16(int mExpense16) {
		this.mExpense16 = mExpense16;
	}

	public int getmExpense17() {
		return mExpense17;
	}

	public void setmExpense17(int mExpense17) {
		this.mExpense17 = mExpense17;
	}

	public int getmExpense18() {
		return mExpense18;
	}

	public void setmExpense18(int mExpense18) {
		this.mExpense18 = mExpense18;
	}

	public int getmExpense19() {
		return mExpense19;
	}

	public void setmExpense19(int mExpense19) {
		this.mExpense19 = mExpense19;
	}

	public int getmExpense20() {
		return mExpense20;
	}

	public void setmExpense20(int mExpense20) {
		this.mExpense20 = mExpense20;
	}

	@Override
	public String toString() {
		return "ManpowerExpense [mExpense16=" + mExpense16 + ", mExpense17=" + mExpense17 + ", mExpense18=" + mExpense18
				+ ", mExpense19=" + mExpense19 + ", mExpense20=" + mExpense20 + "]";
	}
	
	

}
